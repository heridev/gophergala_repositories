# Para ejecutar el programa, copia este código en
# un archivo llamado `hola-mundo.go` y ejecuta `go run`.
$ go run hello-world.go
hello world

# En algunas ocasiones vamos a querer compilar nuestros
# programas en binarios. Esto lo podemos hacer utilizando
# el comando `go build`.
$ go build hello-world.go
$ ls
hello-world  hello-world.go

# Podemos ejecutar el binario directamente.
$ ./hello-world
hello world

